function translateR18Log(template, p) {
    if (!p || !p.genital || !p.chest) return template;
    const g = p.genital;
    const c = p.chest;
    return template
        .replace(/\[SIZE\]/g, `<span style="color:#e91e63; font-weight:bold;">${g.size}</span>`)
        .replace(/\[HARDNESS\]/g, g.hardness)
        .replace(/\[GLANS_SHAPE\]/g, g.glans.shape)
        .replace(/\[GLANS_COLOR\]/g, g.glans.color)
        .replace(/\[GLANS_FEAT\]/g, g.glans.feature) // 对应词库里的 feature
        .replace(/\[SHAFT\]/g, g.shaft)
        .replace(/\[SCROTUM\]/g, g.scrotum) // 对应词库里的 scrotum
        .replace(/\[FLUID\]/g, g.fluid)
        .replace(/\[CHEST_TYPE\]/g, c.type)
        .replace(/\[MUSCLE\]/g, c.muscle)
        .replace(/\[SKIN\]/g, c.skin)
        .replace(/\[NIPPLE\]/g, c.nipple)
        .replace(/\[HAIR\]/g, c.hair)
        .replace(/\[FULL_DESC\]/g, getGenitalFullDesc(p)); // [新增] 支持一键调用整段描述
}
